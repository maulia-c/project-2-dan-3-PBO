public class PersegiPanjang {
    // hitung luas
    int hitungLuas(int p, int l){
        return p * l;
    }
    double hitungLuas(double p, double l){
        return p * l;
    }
    
    // hitung Keliling
    int hitungKeliling(int p, int l){
        return 2 * (p+l);
    }
    double hitungKeliling(double p, double l){
        return 2 * (p+l);
    }
}
